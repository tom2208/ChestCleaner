    /***
     * Compares items against predefined sorting list
     * @param item1 item in question
     * @param item2 item compared against
     * @return true if item1 appears later in the sorting list than item2
     *         items not in the list are greater than items in the list
     *         items not in the list are sorted by first character.
     */
    public boolean isGreaterThan(ItemStack item1, ItemStack item2) {
        int index1 = SORTING_LIST.indexOf(item1.getType());
        int index2 = SORTING_LIST.indexOf(item2.getType());

        if (index1 > -1 && index2 > -1) {
            return SORTING_LIST.indexOf(item1.getType()) > SORTING_LIST.indexOf(item2.getType());
        } else if (index1 < 0 && index2 < 0) {
            return item1.getType().name().compareTo(item2.getType().name()) > 0;
        } else {
            return index2 > -1;
        }
    }

    public boolean isSmallerThan(ItemStack item1, ItemStack item2) {
        return isGreaterThan(item2, item1);
    }
